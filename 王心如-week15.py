
import turtle 
shelly = turtle.Turtle() 
shelly.shape("turtle")
shelly.position()

shelly.color("black", "green") 

shelly.pencolor("brown")

shelly.turtlesize(7,9,2) 
shelly.resizemode("auto")
shelly.turtlesize(5,5,3) 
shelly.forward(200)

shelly.left(90)

shelly.penup()

shelly.pendown()
shelly.hideturtle()
shelly.begin_fill()
shelly.fillcolor("pink")
shelly.circle(250) 
shelly.circle(250, 180, 30) 
shelly.end_fill()

shelly.stamp()

shelly.write("周雅杰我愛你")